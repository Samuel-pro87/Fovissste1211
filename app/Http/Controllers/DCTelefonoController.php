    {
         //return $request->all();
        $tel = new fovi_detalle_telefono();

        $tel->rfc = $request->rfc;
        $tel->ejecutivo = $request->ejecutivo;
        $tel->telefono = $request->telefono;
        $tel->tipo = $request->tipo;
        $tel->status = $request->status;
        $tel->extension = $request->ext;

        $tel->save();
